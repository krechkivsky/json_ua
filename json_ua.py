    def _schema_geometry_wkb(self, class_key: str, schema: dict, common_schema: dict):
        class_key = self._schema_class_key(class_key)
        feature = schema.get("$defs", {}).get(f"{class_key}Feature")
        if not isinstance(feature, dict):
            return None
        geom_ref = None
        for subschema in feature.get("allOf", []):
            if not isinstance(subschema, dict):
                continue
            geom_schema = subschema.get("properties", {}).get("geometry")
            if isinstance(geom_schema, dict):
                geom_ref = geom_schema.get("$ref")
                if geom_ref:
                    break
        if not geom_ref:
            return QgsWkbTypes.NoGeometry
        ref_schema = self._resolve_ref(geom_ref, schema, common_schema)
        geom_name = None
        if isinstance(ref_schema, dict):
            geom_name = ref_schema.get("properties", {}).get("type", {}).get("const")
        if not geom_name:
            geom_name = geom_ref.split("#")[-1].split("/")[-1]
        if geom_name in ("None", "NoGeometry", "Null", "null"):
            return QgsWkbTypes.NoGeometry
        mapping = {
            "Point": QgsWkbTypes.Point,
            "LineString": QgsWkbTypes.LineString,
            "Polygon": QgsWkbTypes.Polygon,
            "MultiPoint": QgsWkbTypes.MultiPoint,
            "MultiLineString": QgsWkbTypes.MultiLineString,
            "MultiPolygon": QgsWkbTypes.MultiPolygon,
        }
        return mapping.get(geom_name, QgsWkbTypes.NoGeometry)

    def _layer_category_from_wkb(self, wkb_type) -> str:
        if wkb_type is None:
            return "metadata"
        try:
            geom_type = QgsWkbTypes.geometryType(wkb_type)
        except Exception:
            return "metadata"
        if geom_type == QgsWkbTypes.PointGeometry:
            return "points"
        if geom_type == QgsWkbTypes.LineGeometry:
            return "lines"
        if geom_type == QgsWkbTypes.PolygonGeometry:
            return "polygons"
        return "metadata"

    def _detect_geojson_category_from_file(self, source_path: str) -> str:
        data = self._read_json_file(source_path)
        if not isinstance(data, dict):
            return "metadata"
        features = data.get("features")
        if not isinstance(features, list):
            return "metadata"
        for feature in features:
            if not isinstance(feature, dict):
                continue
            geometry = feature.get("geometry")
            if not isinstance(geometry, dict):
                continue
            geom_type = geometry.get("type")
            if geom_type in ("Point", "MultiPoint"):
                return "points"
            if geom_type in ("LineString", "MultiLineString"):
                return "lines"
            if geom_type in ("Polygon", "MultiPolygon"):
                return "polygons"
            if geom_type == "GeometryCollection":
                geometries = geometry.get("geometries", [])
                if not isinstance(geometries, list):
                    continue
                for entry in geometries:
                    if not isinstance(entry, dict):
                        continue
                    entry_type = entry.get("type")
                    if entry_type in ("Point", "MultiPoint"):
                        return "points"
                    if entry_type in ("LineString", "MultiLineString"):
                        return "lines"
                    if entry_type in ("Polygon", "MultiPolygon"):
                        return "polygons"
        return "metadata"

    def _categorize_geojson_file(self, class_key: str, schema: dict, common_schema: dict, source_path: str) -> str:
        wkb_type = self._schema_geometry_wkb(class_key, schema, common_schema)
        category = self._layer_category_from_wkb(wkb_type)
        if category != "metadata":
            return category
        return self._detect_geojson_category_from_file(source_path)

    def _format_duration_hhmmss(self, seconds: float) -> str:
        try:
            total = int(max(0, round(float(seconds))))
        except Exception:
            total = 0
        hours, rest = divmod(total, 3600)
        minutes, secs = divmod(rest, 60)
        return f"{hours:02d}:{minutes:02d}:{secs:02d}"

    def _wkb_from_geojson_geometry_type(self, geom_type_name: str):
        mapping = {
            "Point": QgsWkbTypes.Point,
            "LineString": QgsWkbTypes.LineString,
            "Polygon": QgsWkbTypes.Polygon,
            "MultiPoint": QgsWkbTypes.MultiPoint,
            "MultiLineString": QgsWkbTypes.MultiLineString,
            "MultiPolygon": QgsWkbTypes.MultiPolygon,
        }
        return mapping.get(geom_type_name)

    def _geojson_geometry_to_wkt(self, geometry_data: dict):
        if not isinstance(geometry_data, dict):
            return None
        geom_type = geometry_data.get("type")
        if not isinstance(geom_type, str):
            return None
        coords = geometry_data.get("coordinates")

        def fmt_num(value):
            try:
                return f"{float(value):.15g}"
            except Exception:
                return None

        def point_wkt(point):
            if not isinstance(point, (list, tuple)) or len(point) < 2:
                return None
            x = fmt_num(point[0])
            y = fmt_num(point[1])
            if x is None or y is None:
                return None
            return f"{x} {y}"

        def linestring_wkt(line):
            if not isinstance(line, (list, tuple)) or not line:
                return None
            pts = []
            for point in line:
                p = point_wkt(point)
                if p is None:
                    return None
                pts.append(p)
            return ", ".join(pts)

        def polygon_wkt(poly):
            if not isinstance(poly, (list, tuple)) or not poly:
                return None
            rings = []
            for ring in poly:
                ls = linestring_wkt(ring)
                if ls is None:
                    return None
                rings.append(f"({ls})")
            return ", ".join(rings)

        if geom_type == "Point":
            p = point_wkt(coords)
            return f"POINT ({p})" if p else None
        if geom_type == "LineString":
            ls = linestring_wkt(coords)
            return f"LINESTRING ({ls})" if ls else None
        if geom_type == "Polygon":
            poly = polygon_wkt(coords)
            return f"POLYGON ({poly})" if poly else None
        if geom_type == "MultiPoint":
            if not isinstance(coords, (list, tuple)) or not coords:
                return None
            points = []
            for point in coords:
                p = point_wkt(point)
                if p is None:
                    return None
                points.append(f"({p})")
            return f"MULTIPOINT ({', '.join(points)})"
        if geom_type == "MultiLineString":
            if not isinstance(coords, (list, tuple)) or not coords:
                return None
            lines = []
            for line in coords:
                ls = linestring_wkt(line)
                if ls is None:
                    return None
                lines.append(f"({ls})")
            return f"MULTILINESTRING ({', '.join(lines)})"
        if geom_type == "MultiPolygon":
            if not isinstance(coords, (list, tuple)) or not coords:
                return None
            polygons = []
            for poly in coords:
                pw = polygon_wkt(poly)
                if pw is None:
                    return None
                polygons.append(f"({pw})")
            return f"MULTIPOLYGON ({', '.join(polygons)})"
        if geom_type == "GeometryCollection":
            geometries = geometry_data.get("geometries")
            if not isinstance(geometries, (list, tuple)) or not geometries:
                return None
            parts = []
            for entry in geometries:
                wkt = self._geojson_geometry_to_wkt(entry)
                if not wkt:
                    continue
                parts.append(wkt)
            if not parts:
                return None
            return f"GEOMETRYCOLLECTION ({', '.join(parts)})"
        return None

    def _geometry_from_geojson_dict(self, geometry_data: dict):
        if not isinstance(geometry_data, dict):
            return None
        wkt = self._geojson_geometry_to_wkt(geometry_data)
        if not wkt:
            return None
        try:
            geometry = QgsGeometry.fromWkt(wkt)
        except Exception:
            return None
        try:
            if geometry is None or geometry.isNull() or geometry.isEmpty():
                return None
        except Exception:
            pass
        return geometry

    def _schema_simple_type(self, schema_node, schema: dict, common_schema: dict):
        if not isinstance(schema_node, dict):
            return None
        if "$ref" in schema_node:
            return self._schema_simple_type(self._resolve_ref(schema_node["$ref"], schema, common_schema), schema, common_schema)
        if "type" in schema_node:
            schema_type = schema_node["type"]
            if isinstance(schema_type, list):
                for candidate in schema_type:
                    if isinstance(candidate, str):
                        return candidate
            elif isinstance(schema_type, str):
                return schema_type
        for key in ("allOf", "oneOf", "anyOf"):
            if key in schema_node:
                for part in schema_node[key]:
                    result = self._schema_simple_type(part, schema, common_schema)
                    if result:
                        return result
        return None

    def _qvariant_for_schema(self, schema_node, schema: dict, common_schema: dict):
        schema_type = self._schema_simple_type(schema_node, schema, common_schema)
        if schema_type == "integer":
            return QVariant.Int
        if schema_type == "number":
            return QVariant.Double
        if schema_type == "boolean":
            return QVariant.Bool
        if schema_type == "string":
            return QVariant.String
        if schema_type in ("array", "object"):
            return QVariant.String
        return QVariant.String

    def _fields_from_schema(self, class_key: str, schema: dict, common_schema: dict):
        props_schema = self._collect_properties_schema(class_key, schema)
        if not props_schema:
            return None
        fields = QgsFields()
        for name, prop_schema in props_schema.items():
            if self._is_internal_qgis_property(name):
                continue
            fields.append(QgsField(name, self._qvariant_for_schema(prop_schema, schema, common_schema)))
        return fields

    def _get_layer_for_class(self, class_key: str):
        layer_id = self.class_layers.get(class_key)
        if not layer_id:
            return None
        layer = QgsProject.instance().mapLayer(layer_id)
        if layer is None:
            self.class_layers.pop(class_key, None)
            self.layer_registry.pop(layer_id, None)
        return layer

    def _add_layer_to_project_group(self, layer) -> None:
        project = QgsProject.instance()
        group = getattr(self.opened_projects, "current_project", None)
        if group is None:
            project.addMapLayer(layer)
            return
        project.addMapLayer(layer, False)
        try:
            group.addLayer(layer)
        except Exception:
            project.addMapLayer(layer)

    def _register_layer(self, layer, class_name: str, source_path: str, geometry_type, loaded_from_disk: bool) -> None:
        if layer is None:
            return
        meta = {
            "class_name": class_name,
            "source_path": source_path,
            "dirty": False,
            "geometry_type": geometry_type,
            "loaded_from_disk": loaded_from_disk,
            "spatial_index": None,
        }
        self.layer_registry[layer.id()] = meta
        self.class_layers[class_name] = layer.id()
        self._connect_layer_signals(layer)
        self._update_save_action()

    def _connect_layer_signals(self, layer) -> None:
        layer_id = layer.id()
        try:
            layer.featureAdded.connect(lambda _fid, lid=layer_id: self.mark_dirty(lid))
        except Exception:
            pass
        try:
            layer.featureDeleted.connect(lambda _fid, lid=layer_id: self.mark_dirty(lid))
        except Exception:
            pass
        try:
            layer.geometryChanged.connect(lambda _fid, _geom, lid=layer_id: self.mark_dirty(lid))
        except Exception:
            pass
        try:
            layer.attributeValueChanged.connect(lambda _fid, _idx, _val, lid=layer_id: self.mark_dirty(lid))
        except Exception:
            pass
        try:
            layer.editCommandEnded.connect(lambda lid=layer_id: self._on_layer_edit_command_ended(lid))
        except Exception:
            pass
        try:
            layer.beforeCommitChanges.connect(lambda _stop=False, lid=layer_id: self._on_layer_before_commit(lid))
        except Exception:
            pass
        try:
            layer.afterCommitChanges.connect(lambda lid=layer_id: self._on_layer_after_commit(lid))
        except Exception:
            pass
        # Provider-level commit notifications (attributes/geometry/features) are
        # the most reliable trigger after user presses QGIS "Save Layer Edits".
        try:
            layer.committedAttributeValuesChanges.connect(
                lambda *_args, lid=layer_id: self._schedule_layer_commit_save(lid)
            )
        except Exception:
            pass
        try:
            layer.committedGeometriesChanges.connect(
                lambda *_args, lid=layer_id: self._schedule_layer_commit_save(lid)
            )
        except Exception:
            pass
        try:
            layer.committedFeaturesAdded.connect(
                lambda *_args, lid=layer_id: self._schedule_layer_commit_save(lid)
            )
        except Exception:
            pass
        try:
            layer.committedFeaturesRemoved.connect(
                lambda *_args, lid=layer_id: self._schedule_layer_commit_save(lid)
            )
        except Exception:
            pass
        try:
            layer.editingStopped.connect(lambda lid=layer_id: self._on_layer_editing_stopped(lid))
        except Exception:
            pass
        try:
            layer.afterRollBack.connect(lambda lid=layer_id: self._on_layer_after_rollback(lid))
        except Exception:
            pass
        try:
            layer.nameChanged.connect(lambda *_args, lid=layer_id: self._on_layer_name_changed(lid))
        except Exception:
            pass

    def mark_dirty(self, layer_id: str, dirty: bool = True) -> None:
        meta = self.layer_registry.get(layer_id)
        if meta is None:
            return
        meta["dirty"] = bool(dirty)
        self._update_save_action()

    def _update_save_action(self) -> None:
        action = getattr(self, "action_save", None)
        if action is None:
            return
        has_layers = bool(self.layer_registry)
        has_dirty = any(meta.get("dirty") for meta in self.layer_registry.values())
        action.setEnabled(has_layers or has_dirty)

    def build_index(self, layer) -> None:
        if layer is None:
            return
        try:
            index = QgsSpatialIndex(layer.getFeatures())
        except Exception:
            return
        meta = self.layer_registry.get(layer.id())
        if meta is not None:
            meta["spatial_index"] = index

    def _ensure_memory_layer_for_class(self, class_key: str, project_dir: str, source_layer=None, source_path: str = "", loaded_from_disk: bool = False, source_wkb=None):
        existing = self._get_layer_for_class(class_key)
        if existing is not None:
            self._apply_enum_widgets(existing, class_key)
            self._apply_date_widgets(existing, class_key)
            self._apply_year_term_placeholders(existing, class_key)
            return existing
        schema, common_schema = self._load_schema_cache()
        wkb_type = self._schema_geometry_wkb(class_key, schema, common_schema)
        if wkb_type is None and source_wkb is not None:
            wkb_type = source_wkb
        if wkb_type is None and source_layer is not None:
            try:
                wkb_type = source_layer.wkbType()
            except Exception:
                wkb_type = None
        if wkb_type is None:
            wkb_type = QgsWkbTypes.Point
        fields = self._fields_from_schema(class_key, schema, common_schema)
        if fields is None or fields.count() == 0:
            if source_layer is not None:
                try:
                    fields = source_layer.fields()
                except Exception:
                    fields = QgsFields()
            else:
                fields = QgsFields()
        layer = self._create_memory_layer(class_key, wkb_type, fields)
        if layer is None:
            return None
        if not source_path and project_dir:
            source_path = os.path.join(project_dir, f"{class_key}.geojson")
        self._register_layer(layer, class_key, source_path, wkb_type, loaded_from_disk)
        self._add_layer_to_project_group(layer)
        return layer

    def _create_memory_layer(self, class_key: str, wkb_type, fields: QgsFields):
        if wkb_type == QgsWkbTypes.NoGeometry:
            uri = "None"
        else:
            geom_name = QgsWkbTypes.displayString(wkb_type) if wkb_type is not None else "Geometry"
            crs = QgsProject.instance().crs()
            uri = f"{geom_name}?crs={crs.authid()}" if crs.isValid() else geom_name
        layer = QgsVectorLayer(uri, class_key, "memory")
        if not layer.isValid():
            return None
        try:
            if wkb_type != QgsWkbTypes.NoGeometry:
                geom_type = QgsWkbTypes.geometryType(layer.wkbType())
                if geom_type == QgsWkbTypes.PolygonGeometry:
                    symbol = QgsFillSymbol.createSimple({
                        "color": "0,0,0,0",
                        "outline_color": "0,0,0,255",
                        "outline_width": "0.3",
                    })
                elif geom_type == QgsWkbTypes.LineGeometry:
                    symbol = QgsLineSymbol.createSimple({
                        "color": "0,0,0,255",
                        "width": "0.3",
                    })
                else:
                    symbol = QgsMarkerSymbol.createSimple({
                        "color": "0,0,0,255",
                        "outline_color": "0,0,0,255",
                        "size": "2.0",
                    })
                if symbol is not None:
                    layer.renderer().setSymbol(symbol)
        except Exception:
            pass
        try:
            layer.dataProvider().addAttributes(fields)
            layer.updateFields()
        except Exception:
            pass
        self._apply_enum_widgets(layer, class_key)
        self._apply_date_widgets(layer, class_key)
        self._apply_year_term_placeholders(layer, class_key)
        try:
            layer.startEditing()
        except Exception:
            pass
        return layer

    def _geometry_compatible(self, target_wkb, source_wkb) -> bool:
        if QgsWkbTypes.geometryType(target_wkb) != QgsWkbTypes.geometryType(source_wkb):
            return False
        if QgsWkbTypes.isMultiType(source_wkb) and not QgsWkbTypes.isMultiType(target_wkb):
            return False
        return True

    def _coerce_geometry(self, geometry, target_wkb):
        if geometry is None:
            return None
        try:
            new_geom = QgsGeometry(geometry)
        except Exception:
            return None
        if QgsWkbTypes.isMultiType(target_wkb) and not QgsWkbTypes.isMultiType(new_geom.wkbType()):
            try:
                new_geom.convertToMultiType()
            except Exception:
                pass
        return new_geom

    def _find_source_layer_for_selection(self):
        layer = None
        if self.iface is not None:
            try:
                layer = self.iface.activeLayer()
            except Exception:
                layer = None
        if isinstance(layer, QgsVectorLayer):
            return layer
        selected_layers = []
        try:
            for candidate in QgsProject.instance().mapLayers().values():
                if not isinstance(candidate, QgsVectorLayer):
                    continue
                try:
                    if candidate.selectedFeatureCount() > 0:
                        selected_layers.append(candidate)
                except Exception:
                    continue
        except Exception:
            selected_layers = []
        if selected_layers:
            return selected_layers[0]
        return None

    def _fix_syntax_for_selected_layer(self, layer_id: str, feature_id=None) -> None:
        if not layer_id:
            return
        layer = QgsProject.instance().mapLayer(layer_id)
        if layer is None or not isinstance(layer, QgsVectorLayer):
            self._push_message(self.tr(u"Шар для виправлення синтаксису не знайдено."), level=Qgis.Warning)
            return
        meta = self.layer_registry.get(layer_id)
        class_key = (meta or {}).get("class_name") if isinstance(meta, dict) else None
        if not class_key:
            try:
                class_key = layer.name()
            except Exception:
                class_key = None
        if not class_key:
            self._push_message(self.tr(u"Не вдалося визначити клас шару для перевірки синтаксису."), level=Qgis.Warning)
            return

        schema, common_schema = self._load_schema_cache()
        props_schema = self._collect_properties_schema(class_key, schema)
        if not props_schema:
            self._push_message(self.tr(u"Схема для шару {0} не знайдена.").format(class_key), level=Qgis.Warning)
            return

        expected = {}
        expected_order = []
        for field_name, schema_node in props_schema.items():
            normalized = self._normalize_cyrillic_key(field_name)
            if normalized.lower() in IGNORED_INTERNAL_PROPERTIES:
                continue
            if normalized in expected:
                continue
            expected[normalized] = (field_name, schema_node)
            expected_order.append(normalized)
        if self._uses_state_change(class_key):
            for extra_name in ("state", "change"):
                if extra_name not in expected:
                    expected[extra_name] = (extra_name, {"type": "string"})
                    expected_order.append(extra_name)
        expected_norm = set(expected.keys())

        existing_fields = layer.fields()
        normalized_to_index = {}
        extra_indices = []
        for index in range(existing_fields.count()):
            name = existing_fields.at(index).name()
            normalized = self._normalize_cyrillic_key(name)
            if normalized.lower() in IGNORED_INTERNAL_PROPERTIES:
                continue
            if normalized in expected_norm:
                normalized_to_index.setdefault(normalized, index)
            else:
                extra_indices.append(index)

        add_fields = []
        added_field_names = []
        for normalized in expected_order:
            if normalized in normalized_to_index:
                continue
            canonical_name, schema_node = expected[normalized]
            add_fields.append(QgsField(canonical_name, self._qvariant_for_schema(schema_node, schema, common_schema)))
            added_field_names.append(canonical_name)

        try:
            if not layer.isEditable():
                layer.startEditing()
        except Exception:
            pass

        added_count = 0
        removed_count = 0
        if add_fields:
            try:
                if layer.dataProvider().addAttributes(add_fields):
                    layer.updateFields()
                    added_count = len(add_fields)
            except Exception:
                pass
        if extra_indices:
            try:
                if layer.dataProvider().deleteAttributes(sorted(extra_indices, reverse=True)):
                    layer.updateFields()
                    removed_count = len(extra_indices)
            except Exception:
                pass

        # Prefill newly added attributes on the selected feature with schema defaults.
        if feature_id is not None and added_field_names:
            info = self._current_project_info()
            katotth = getattr(info, "katotth", "") if info is not None else ""
            defaults = self._build_feature_properties(class_key, katotth, schema, common_schema) or {}
            attrs = {}
            current_fields = layer.fields()
            for field_name in added_field_names:
                idx = current_fields.lookupField(field_name)
                if idx < 0:
                    continue
                attrs[idx] = defaults.get(field_name)
            if attrs:
                try:
                    layer.dataProvider().changeAttributeValues({feature_id: attrs})
                except Exception:
                    pass

        self.build_index(layer)
        self.mark_dirty(layer.id(), True)
        if added_count or removed_count:
            self._push_info(
                self.tr(u"Синтаксис виправлено для шару {0}: додано полів {1}, видалено полів {2}.").format(
                    class_key,
                    added_count,
                    removed_count,
                )
            )
        else:
            self._push_info(self.tr(u"Синтаксис шару {0} вже коректний.").format(class_key))

    def append_selected_to_class(self, class_key: str) -> None:
        if not class_key:
            return
        info = self._current_project_info()
        project_dir = self._resolve_project_dir(info)
        if not project_dir:
            self._push_message(self.tr(u"Оберіть групу проєкту, щоб додати об'єкти."), level=Qgis.Warning)
            return

        schema, common_schema = self._load_schema_cache()
        source_layer = self._find_source_layer_for_selection()
        try:
            selected = source_layer.selectedFeatures() if source_layer is not None else []
        except Exception:
            selected = []

        target_class_key = self._resolve_class_key_for_creation(class_key, info=info)
        target_layer = self._ensure_memory_layer_for_class(target_class_key, project_dir, source_layer=source_layer)
        if target_layer is None:
            self._push_message(self.tr(u"Не вдалося створити memory-layer для {0}.").format(class_key), level=Qgis.Critical)
            return

        target_is_table = QgsWkbTypes.geometryType(target_layer.wkbType()) == QgsWkbTypes.NullGeometry
        if source_layer is None and not target_is_table:
            self._push_message(self.tr(u"Немає активного векторного шару з виділенням."), level=Qgis.Warning)
            return
        if not selected and not target_is_table:
            self._push_message(self.tr(u"Немає виділених об'єктів для копіювання."), level=Qgis.Warning)
            return

        if source_layer is not None and not target_is_table and not self._geometry_compatible(target_layer.wkbType(), source_layer.wkbType()):
            self._push_message(self.tr(u"Тип геометрії не відповідає шару {0}.").format(class_key), level=Qgis.Warning)
            return

        katotth = getattr(info, "katotth", "") if info is not None else ""
        props_template = {}
        if schema and f"{self._schema_class_key(class_key)}Feature" in schema.get("$defs", {}):
            props_template = self._build_feature_properties(class_key, katotth, schema, common_schema)
        if not props_template:
            self._push_message(self.tr(u"Немає схеми для шару {0}. Атрибути не можуть бути згенеровані.").format(class_key), level=Qgis.Warning)
            return

        target_fields = target_layer.fields()
        new_features = []
        source_features = selected if selected else ([None] if target_is_table else [])
        for feature in source_features:
            props = dict(props_template)

            new_feature = QgsFeature(target_fields)
            if not target_is_table and feature is not None:
                geometry = self._coerce_geometry(feature.geometry(), target_layer.wkbType())
                if geometry is not None:
                    new_feature.setGeometry(geometry)
            attributes = [props.get(field.name()) for field in target_fields]
            new_feature.setAttributes(attributes)
            new_features.append(new_feature)

        try:
            if not target_layer.isEditable():
                target_layer.startEditing()
        except Exception:
            pass
        try:
            ok, added = target_layer.dataProvider().addFeatures(new_features)
        except Exception:
            self._push_message(self.tr(u"Не вдалося додати об'єкти у шар {0}.").format(class_key), level=Qgis.Critical)
            return
        if not ok:
            self._push_message(self.tr(u"Не вдалося додати об'єкти у шар {0}.").format(class_key), level=Qgis.Critical)
            return
        target_layer.updateExtents()
        self.build_index(target_layer)
        self.mark_dirty(target_layer.id(), True)
        try:
            if source_layer is not None:
                source_layer.removeSelection()
        except Exception:
            pass
        try:
            added_ids = [feature.id() for feature in added if feature.id() is not None]
            if added_ids:
                target_layer.selectByIds(added_ids)
        except Exception:
            pass
        self._push_info(self.tr(u"Додано {0} об'єктів у {1}. Нові об'єкти виділено.").format(len(new_features), class_key))

    def load_folder(self, folder_path: str) -> None:
        if not folder_path or not os.path.isdir(folder_path):
            return
        file_names = sorted(
            name for name in os.listdir(folder_path)
            if name.lower().endswith(".geojson")
        )
        if not file_names:
            return

        schema, common_schema = self._load_schema_cache()
        categories = {key: [] for key in ("metadata", "points", "lines", "polygons")}
        total_kb = 0
        for name in file_names:
            source_path = os.path.join(folder_path, name)
            class_key = os.path.splitext(name)[0]
            try:
                size_bytes = os.path.getsize(source_path)
            except Exception:
                size_bytes = 0
            size_kb = max(1, int((size_bytes + 1023) // 1024))
            category = self._categorize_geojson_file(class_key, schema, common_schema, source_path)
            categories.setdefault(category, []).append(
                {
                    "name": name,
                    "class_key": class_key,
                    "source_path": source_path,
                    "size_kb": size_kb,
                    "category": category,
                }
            )
            total_kb += size_kb

        for key in categories:
            categories[key].sort(key=lambda item: item["name"].lower())
        ordered_files = []
        for key in ("metadata", "points", "lines", "polygons"):
            ordered_files.extend(categories.get(key, []))
        if not ordered_files:
            return

        progress = QProgressDialog(
            self.tr(u"Відкриття проєкту..."),
            self.tr(u"Скасувати"),
            0,
            max(total_kb, 1),
            self.iface.mainWindow(),
        )
        progress.setWindowModality(Qt.ApplicationModal)
        progress.setMinimumDuration(0)
        progress.setAutoClose(False)
        progress.setAutoReset(False)
        progress.setValue(0)
        self._align_progress_dialog_left(progress)
        self._pump_progress_ui(progress, force=True)

        loaded = 0
        elapsed_start = time.monotonic()
        processed_kb = 0.0
        last_ui_pump_at = 0.0

        def update_progress(file_info, stage_text: str, fraction: float) -> None:
            nonlocal last_ui_pump_at
            if file_info is None:
                return
            size_kb = float(max(1, int(file_info.get("size_kb", 1))))
            fraction = max(0.0, min(1.0, float(fraction)))
            done_kb = min(float(total_kb), processed_kb + size_kb * fraction)
            elapsed = max(0.0, time.monotonic() - elapsed_start)
            if done_kb > 0.0:
                remaining_kb = max(0.0, float(total_kb) - done_kb)
                eta = (elapsed / done_kb) * remaining_kb
            else:
                eta = 0.0
            line_1 = self.tr(u"Минуло: {0} | Очікується: {1}").format(
                self._format_duration_hhmmss(elapsed),
                self._format_duration_hhmmss(eta),
            )
            line_2 = self.tr(u"{0} {1} кБ {2}").format(
                file_info.get("name", ""),
                int(size_kb),
                stage_text,
            )
            progress.setLabelText(f"{line_1}\n{line_2}")
            progress.setValue(int(max(0.0, min(float(total_kb), done_kb))))
            now = time.monotonic()
            if (now - last_ui_pump_at) >= 0.12 or fraction >= 0.999:
                self._pump_progress_ui(progress)
                last_ui_pump_at = now

        canvas = self.iface.mapCanvas() if getattr(self, "iface", None) is not None else None
        prev_render = None
        if canvas is not None:
            try:
                prev_render = canvas.renderFlag()
                canvas.setRenderFlag(False)
            except Exception:
                prev_render = None
        cpu_boost_token = self._enter_cpu_boost_mode()
        self._bulk_load_in_progress = True

        try:
            for file_info in ordered_files:
                if progress.wasCanceled():
                    break
                name = file_info["name"]
                class_key = file_info["class_key"]
                file_size_kb = int(file_info.get("size_kb", 0))
                if file_size_kb > 500:
                    self._refresh_cpu_boost_mode(cpu_boost_token)
                if self._get_layer_for_class(class_key) is not None:
                    processed_kb += float(file_info["size_kb"])
                    update_progress(file_info, self.tr(u"Пропуск (вже додано)"), 1.0)
                    continue

                source_path = file_info["source_path"]
                update_progress(file_info, self.tr(u"Читання з диску"), 0.10)
                item = None
                parse_task = _GeoJsonParseTask(source_path, class_key)
                parse_state = {
                    "done": False,
                    "success": False,
                    "records": [],
                    "source_field_names": set(),
                    "geometry_type": None,
                    "error_message": "",
                }

                def _on_parse_completed():
                    try:
                        parse_state["success"] = bool(parse_task.success)
                        parse_state["records"] = list(parse_task.records or [])
                        parse_state["source_field_names"] = set(parse_task.source_field_names or set())
                        parse_state["geometry_type"] = parse_task.geometry_type
                        parse_state["error_message"] = parse_task.error_message or ""
                    except Exception:
                        parse_state["success"] = False
                    parse_state["done"] = True

                def _on_parse_terminated():
                    parse_state["done"] = True
                    parse_state["success"] = False

                try:
                    parse_task.taskCompleted.connect(_on_parse_completed)
                except Exception:
                    pass
                try:
                    parse_task.taskTerminated.connect(_on_parse_terminated)
                except Exception:
                    pass
                try:
                    QgsApplication.taskManager().addTask(parse_task)
                except Exception:
                    parse_task = None
                parse_started_at = time.monotonic()
                while parse_task is not None and not parse_state["done"]:
                    if progress.wasCanceled():
                        try:
                            parse_task.cancel()
                        except Exception:
                            pass
                        break
                    parse_progress = 0.0
                    try:
                        parse_progress = float(parse_task.progress())
                    except Exception:
                        parse_progress = 0.0
                    update_progress(file_info, self.tr(u"Синтаксичний аналіз"), 0.55 + 0.20 * min(1.0, parse_progress / 100.0))
                    if file_size_kb > 500 and (time.monotonic() - parse_started_at) >= 0.35:
                        self._refresh_cpu_boost_mode(cpu_boost_token)
                        parse_started_at = time.monotonic()
                    # Avoid busy-wait loop while task is running.
                    time.sleep(0.01)
                if progress.wasCanceled():
                    break
                if parse_task is None or not parse_state["success"]:
                    self._close_status(item)
                    processed_kb += float(file_info["size_kb"])
                    update_progress(file_info, self.tr(u"Помилка читання"), 1.0)
                    continue

                update_progress(file_info, self.tr(u"Синтаксичний аналіз"), 0.55)
                source_wkb = self._wkb_from_geojson_geometry_type(parse_state["geometry_type"])
                target_layer = self._ensure_memory_layer_for_class(
                    class_key,
                    folder_path,
                    source_layer=None,
                    source_path=source_path,
                    loaded_from_disk=True,
                    source_wkb=source_wkb,
                )
                if target_layer is None:
                    self._close_status(item)
                    processed_kb += float(file_info["size_kb"])
                    update_progress(file_info, self.tr(u"Помилка створення шару"), 1.0)
                    continue
                props_template = {}
                if schema and f"{self._schema_class_key(class_key)}Feature" in schema.get("$defs", {}):
                    props_template = self._build_feature_properties(class_key, "", schema, common_schema)
                target_fields = target_layer.fields()
                source_field_names = {
                    name for name in set(parse_state["source_field_names"] or set())
                    if not self._is_internal_qgis_property(name)
                }
                new_features = []
                prepared_records = list(parse_state["records"] or [])
                feature_total = len(prepared_records)
                chunk_size = 2000 if file_size_kb > 500 else 5000
                chunk_add_failed = False
                last_boost_at = time.monotonic()

                def flush_features_chunk() -> bool:
                    nonlocal new_features, chunk_add_failed, last_boost_at
                    if not new_features:
                        return True
                    try:
                        if not target_layer.isEditable():
                            target_layer.startEditing()
                    except Exception:
                        pass
                    try:
                        target_layer.dataProvider().addFeatures(new_features)
                        new_features = []
                        if file_size_kb > 500:
                            now_boost = time.monotonic()
                            if (now_boost - last_boost_at) >= 0.35:
                                self._refresh_cpu_boost_mode(cpu_boost_token)
                                last_boost_at = now_boost
                        return True
                    except Exception:
                        chunk_add_failed = True
                        return False

                for feature_index, record in enumerate(prepared_records, start=1):
                    new_feature = QgsFeature(target_fields)
                    geometry_obj = self._geometry_from_geojson_dict(record.get("geometry"))
                    geometry = self._coerce_geometry(geometry_obj, target_layer.wkbType())
                    if geometry is not None:
                        new_feature.setGeometry(geometry)
                    properties = record.get("properties")
                    if not isinstance(properties, dict):
                        properties = {}
                    if self._schema_class_key(class_key) == METADATA_CLASS_KEY:
                        properties = self._normalize_metadata_properties(properties)
                    properties = self._strip_internal_properties(properties)
                    if props_template:
                        props = dict(props_template)
                        for field_name in source_field_names:
                            if field_name in props:
                                try:
                                    props[field_name] = properties.get(field_name)
                                except Exception:
                                    pass
                        self._apply_property_aliases(class_key, props, source_field_names, properties)
                        attributes = [props.get(field.name()) for field in target_fields]
                    else:
                        attributes = [properties.get(field.name()) for field in target_fields]
                    new_feature.setAttributes(attributes)
                    new_features.append(new_feature)
                    if len(new_features) >= chunk_size:
                        if not flush_features_chunk():
                            break
                    if feature_index % 500 == 0:
                        if feature_total > 0:
                            progress_fraction = 0.55 + 0.25 * min(1.0, float(feature_index) / float(feature_total))
                        else:
                            progress_fraction = 0.70
                        update_progress(file_info, self.tr(u"Синтаксичний аналіз"), progress_fraction)
                    if file_size_kb > 500 and feature_index % 2000 == 0:
                        self._refresh_cpu_boost_mode(cpu_boost_token)
                if not chunk_add_failed and not flush_features_chunk():
                    chunk_add_failed = True
                if chunk_add_failed:
                    self._close_status(item)
                    processed_kb += float(file_info["size_kb"])
                    update_progress(file_info, self.tr(u"Помилка додавання об'єктів"), 1.0)
                    continue
                target_layer.updateExtents()
                update_progress(file_info, self.tr(u"Індексація"), 0.85)
                self._update_status(item, self.tr(u"{0}: побудова просторового індекса").format(name))
                self.build_index(target_layer)
                self._close_status(item)
                self.mark_dirty(target_layer.id(), False)
                loaded += 1
                processed_kb += float(file_info["size_kb"])
                update_progress(file_info, self.tr(u"Готово"), 1.0)
        finally:
            self._bulk_load_in_progress = False
            self._leave_cpu_boost_mode(cpu_boost_token)
            if canvas is not None and prev_render is not None:
                try:
                    canvas.setRenderFlag(prev_render)
                except Exception:
                    pass
        progress.setValue(max(total_kb, 1))
        try:
            progress.setCancelButtonText(self.tr(u"Закрити"))
        except Exception:
            pass
        self._pump_progress_ui(progress)
        try:
            progress.close()
            progress.deleteLater()
        except Exception:
            pass
        if loaded:
            pass

    def _on_layer_after_commit(self, layer_id: str) -> None:
        if not layer_id:
            return
        self._schedule_layer_commit_save(layer_id)

    def _on_layer_before_commit(self, layer_id: str) -> None:
        if not layer_id:
            return
        self._pending_layer_commit_save.add(layer_id)

    def _on_layer_after_rollback(self, layer_id: str) -> None:
        if not layer_id:
            return
        self._pending_layer_commit_save.discard(layer_id)

    def _on_layer_editing_stopped(self, layer_id: str) -> None:
        # In some QGIS setups afterCommitChanges may be skipped/delayed.
        # If commit was requested and editing stopped, persist anyway.
        if not layer_id:
            return
        if layer_id in self._pending_layer_commit_save:
            self._schedule_layer_commit_save(layer_id)

    def _schedule_layer_commit_save(self, layer_id: str) -> None:
        if not layer_id:
            return
        if self._bulk_load_in_progress:
            return
        self._pending_layer_commit_save.discard(layer_id)
        if layer_id in self._scheduled_commit_autosaves:
            return
        self._scheduled_commit_autosaves.add(layer_id)
        QTimer.singleShot(120, lambda lid=layer_id: self._autosave_committed_layer(lid))

    def _autosave_committed_layer(self, layer_id: str) -> None:
        self._scheduled_commit_autosaves.discard(layer_id)
        if not layer_id or layer_id not in self.layer_registry:
            return
        self._autosave_to_geojson(layer_id)

    def save_layer(self, layer_id: str, prompt_add_file: bool = False) -> bool:
        meta = self.layer_registry.get(layer_id)
        if meta is None:
            return False
        layer = QgsProject.instance().mapLayer(layer_id)
        if layer is None:
            return False
        source_path = meta.get("source_path", "")
        if self._is_auxiliary_project_layer(layer, source_path):
            meta["dirty"] = False
            return True
        if not source_path:
            info = self._current_project_info()
            project_dir = self._resolve_project_dir(info)
            if project_dir:
                source_path = os.path.join(project_dir, f"{meta.get('class_name', layer.name())}.geojson")
                meta["source_path"] = source_path
        if not source_path:
            self._push_message(self.tr(u"Немає шляху для збереження шару {0}.").format(layer.name()), level=Qgis.Warning)
            return False
        self._close_geojson_layers_for_path(source_path)
        write_path = source_path
        locked = False
        if os.path.exists(source_path):
            try:
                os.remove(source_path)
            except Exception:
                locked = True
        if locked or os.path.exists(source_path):
            base, ext = os.path.splitext(source_path)
            write_path = f"{base}_tmp{ext or '.geojson'}"
        item = None
        options = QgsVectorFileWriter.SaveVectorOptions()
        options.driverName = "GeoJSON"
        options.fileEncoding = "UTF-8"
        options.actionOnExistingFile = QgsVectorFileWriter.CreateOrOverwriteFile
        try:
            # Prevent GDAL/QGIS from writing/deriving "fid" into properties.
            options.layerOptions = ["ID_FIELD="]
        except Exception:
            pass
        result, error = QgsVectorFileWriter.writeAsVectorFormatV2(
            layer,
            write_path,
            QgsProject.instance().transformContext(),
            options,
        )
        self._close_status(item)
        if result != QgsVectorFileWriter.NoError:
            self._push_message(self.tr(u"Помилка збереження {0}: {1}").format(layer.name(), error), level=Qgis.Critical)
            return False
        final_path = source_path
        if write_path != source_path:
            try:
                if os.path.exists(source_path):
                    os.remove(source_path)
                os.replace(write_path, source_path)
            except Exception:
                try:
                    if os.path.exists(write_path):
                        os.remove(write_path)
                except Exception:
                    pass
                self._push_message(
                    self.tr(u"Не вдалося перезаписати існуючий файл {0}.").format(os.path.basename(source_path)),
                    level=Qgis.Critical,
                )
                return False
        meta["dirty"] = False
        meta["loaded_from_disk"] = True
        self._push_info(self.tr(u"Збережено GeoJSON: {0}").format(os.path.basename(final_path)))
        if prompt_add_file:
            self._prompt_add_saved_file(final_path, meta.get("class_name", layer.name()))
        self._update_save_action()
        return True

    def _prompt_add_saved_file(self, source_path: str, class_name: str) -> None:
        parent = self.iface.mainWindow() if self.iface is not None else None
        dialog = QMessageBox(parent)
        dialog.setIcon(QMessageBox.Question)
        dialog.setWindowTitle(self.tr(u"Додати GeoJSON у проєкт"))
        dialog.setText(self.tr(u"Додати збережений GeoJSON файл у проєкт?"))
        add_button = dialog.addButton(self.tr(u"Додати"), QMessageBox.AcceptRole)
        dialog.addButton(self.tr(u"Не додавати"), QMessageBox.RejectRole)
        dialog.setDefaultButton(add_button)
        dialog.exec_()
        if dialog.clickedButton() != add_button:
            return
        if self._geojson_layer_exists(source_path):
            self._push_message(self.tr(u"GeoJSON файл вже додано у проєкт."), level=Qgis.Info)
            return
        layer = QgsVectorLayer(source_path, f"{class_name}_file", "ogr")
        if not layer.isValid():
            self._push_message(self.tr(u"Не вдалося додати GeoJSON файл у проєкт."), level=Qgis.Warning)
            return
        group = getattr(self.opened_projects, "current_project", None)
        project = QgsProject.instance()
        if group is None:
            project.addMapLayer(layer)
        else:
            project.addMapLayer(layer, False)
            try:
                group.addLayer(layer)
            except Exception:
                project.addMapLayer(layer)
        self._push_message(self.tr(u"GeoJSON файл додано."), level=Qgis.Info)

    def _geojson_layer_exists(self, source_path: str) -> bool:
        try:
            layers = QgsProject.instance().mapLayers().values()
        except Exception:
            return False
        for layer in layers:
            try:
                src = layer.source()
            except Exception:
                src = ""
            if source_path in src:
                return True
        return False

    def _close_geojson_layers_for_path(self, source_path: str) -> None:
        if not source_path:
            return
        try:
            project = QgsProject.instance()
            layers = list(project.mapLayers().values())
        except Exception:
            return
        for layer in layers:
            try:
                src = layer.source()
            except Exception:
                src = ""
            if source_path and source_path in src:
                try:
                    project.removeMapLayer(layer.id())
                except Exception:
                    continue

    def save_all_dirty(self) -> bool:
        layers_to_save = self._collect_layers_to_save()
        if not layers_to_save:
            self._push_info(self.tr(u"Немає незбережених змін."))
            return True
        failed = []
        for layer_id in layers_to_save:
            if not self.save_layer(layer_id):
                failed.append(layer_id)
        if failed:
            self._push_message(self.tr(u"Не вдалося зберегти частину шарів."), level=Qgis.Warning)
            return False
        self._push_info(self.tr(u"Усі зміни збережено."))
        return True

    def _prompt_save_dirty(self) -> bool:
        layers_to_save = self._collect_layers_to_save()
        if not layers_to_save:
            return True
        parent = self.iface.mainWindow() if self.iface is not None else None
        dialog = QMessageBox(parent)
        dialog.setIcon(QMessageBox.Warning)
        dialog.setWindowTitle(self.tr(u"Незбережені зміни"))
        dialog.setText(self.tr(u"Є незбережені зміни у memory-layers. Зберегти?"))
        save_button = dialog.addButton(self.tr(u"Зберегти"), QMessageBox.AcceptRole)
        discard_button = dialog.addButton(self.tr(u"Не зберігати"), QMessageBox.DestructiveRole)
        cancel_button = dialog.addButton(self.tr(u"Скасувати"), QMessageBox.RejectRole)
        dialog.setDefaultButton(save_button)
        dialog.exec_()
        clicked = dialog.clickedButton()
        if clicked == save_button:
            return self.save_all_dirty()
        if clicked == discard_button:
            return True
        return False

    def _collect_layers_to_save(self):
        layers = []
        project = QgsProject.instance()
        for layer_id, meta in self.layer_registry.items():
            layer = project.mapLayer(layer_id)
            if layer is None:
                continue
            is_dirty = bool(meta.get("dirty"))
            if not is_dirty:
                try:
                    is_dirty = bool(layer.isModified())
                except Exception:
                    is_dirty = False
            if is_dirty:
                layers.append(layer_id)
        return layers

    def _clear_registry_for_group(self, group) -> None:
        if group is None:
            return
        group_name = ""
        try:
            group_name = group.name() or ""
        except Exception:
            group_name = ""
        layer_ids = []
        try:
            children = group.children()
        except Exception:
            children = []
        for child in children:
            layer = getattr(child, "layer", None)
            if callable(layer):
                try:
                    layer_obj = layer()
                except Exception:
                    layer_obj = None
                if layer_obj is not None:
                    layer_ids.append(layer_obj.id())
        for layer_id in layer_ids:
            meta = self.layer_registry.pop(layer_id, None)
            self._layer_theme_cache.pop(layer_id, None)
            if meta:
                class_name = meta.get("class_name")
                if class_name and self.class_layers.get(class_name) == layer_id:
                    self.class_layers.pop(class_name, None)
        if group_name:
            prefix = f"{group_name}::"
            for key in list(self._layer_theme_cache.keys()):
                if isinstance(key, str) and key.startswith(prefix):
                    self._layer_theme_cache.pop(key, None)
        self._update_save_action()

    def _on_request_geojson(self, class_key: str) -> None:
        if self._should_ignore_non_spatial_table_double_click(class_key):
            return
        self.append_selected_to_class(class_key)

    def _has_prefixed_table_file(self, class_key: str, project_dir: str) -> bool:
        suffix = NON_SPATIAL_TABLE_FILE_SUFFIXES.get((class_key or "").strip())
        if not suffix or not project_dir:
            return False
        lower_suffix = suffix.lower()
        try:
            names = os.listdir(project_dir)
        except Exception:
            return False
        for name in names:
            if not str(name).lower().endswith(lower_suffix):
                continue
            prefix = str(name)[:-len(suffix)]
            if prefix:
                return True
        return False

    def _should_ignore_non_spatial_table_double_click(self, class_key: str) -> bool:
        normalized = (class_key or "").strip()
        if normalized not in NON_SPATIAL_TABLE_FILE_SUFFIXES:
            return False
        if self._get_layer_for_class(normalized) is not None:
            return False
        info = self._current_project_info()
        project_dir = self._resolve_project_dir(info)
        if not project_dir:
            return False
        return self._has_prefixed_table_file(normalized, project_dir)

    def _is_katotth_group_name(self, name: str) -> bool:
        if not name:
            return False
        if name.startswith("old_"):
            return False
        return self._is_project_folder_name(name)

    def _rename_existing_project_groups(self) -> None:
        try:
            root = QgsProject.instance().layerTreeRoot()
        except Exception:
            return
        if root is None:
            return
        for child in root.children():
            is_group = getattr(child, "isGroup", None)
            if not callable(is_group) or not is_group():
                continue
            name = child.name()
            if name.startswith("old_"):
                continue
            if self._is_katotth_group_name(name):
                try:
                    child.setName(f"old_{name}")
                except Exception:
                    pass

