    def _build_sketch_renderer(self, layer: QgsVectorLayer):
        if layer is None:
            return None
        try:
            geom_type = QgsWkbTypes.geometryType(layer.wkbType())
        except Exception:
            return None
        symbol = None
        if geom_type == QgsWkbTypes.PolygonGeometry:
            symbol = QgsFillSymbol.createSimple({
                "color": "0,0,0,0",
                "outline_color": "0,0,0,255",
                "outline_width": "0.3",
            })
        elif geom_type == QgsWkbTypes.LineGeometry:
            symbol = QgsLineSymbol.createSimple({
                "color": "0,0,0,255",
                "width": "0.3",
            })
        else:
            symbol = QgsMarkerSymbol.createSimple({
                "color": "0,0,0,255",
                "outline_color": "0,0,0,255",
                "size": "2.0",
            })
        if symbol is None:
            return None
        return QgsSingleSymbolRenderer(symbol)

    def _apply_normative_style(self, group, enabled: bool) -> None:
        if group is None:
            return
        layers = self._group_layers(group)
        vector_layers = []
        for layer in layers:
            if not isinstance(layer, QgsVectorLayer):
                continue
            try:
                if layer.providerType().lower() != "memory":
                    continue
            except Exception:
                continue
            vector_layers.append(layer)
        total = len(vector_layers)
        if total == 0:
            return
        progress = QProgressDialog(
            self.tr(u"Застосування теми..."),
            self.tr(u"Скасувати"),
            0,
            total,
            self.iface.mainWindow() if self.iface is not None else None,
        )
        progress.setWindowModality(Qt.ApplicationModal)
        progress.setMinimumDuration(0)
        progress.setAutoClose(False)
        progress.setAutoReset(False)
        progress.setValue(0)
        self._align_progress_dialog_left(progress)
        self._pump_progress_ui(progress, force=True)
        canvas = self.iface.mapCanvas() if getattr(self, "iface", None) is not None else None
        prev_render = None
        if canvas is not None:
            try:
                prev_render = canvas.renderFlag()
                canvas.setRenderFlag(False)
            except Exception:
                prev_render = None
        started_at = time.monotonic()
        cache_hits = 0
        file_reads = 0
        sketch_fallbacks = 0

        def update_theme_progress(layer_name: str, index_value: int, stage_text: str) -> None:
            elapsed = max(0.0, time.monotonic() - started_at)
            done = float(max(1, index_value - 1))
            remaining = float(max(0, total - (index_value - 1)))
            eta = (elapsed / done) * remaining if done > 0.0 else 0.0
            progress.setLabelText(
                self.tr(
                    u"РњРёРЅСѓР»Рѕ: {0} | РћС‡С–РєСѓС”С‚СЊСЃСЏ: {1} | РљРµС€: {2} | Р¤Р°Р№Р»: {3} | Р•СЃРєС–Р·: {4}\n{5} ({6}/{7}) - {8}"
                ).format(
                    self._format_duration_hhmmss(elapsed),
                    self._format_duration_hhmmss(eta),
                    cache_hits,
                    file_reads,
                    sketch_fallbacks,
                    layer_name,
                    index_value,
                    total,
                    stage_text,
                )
            )
            progress.setValue(index_value - 1)
            self._pump_progress_ui(progress)

        for index, layer in enumerate(vector_layers, start=1):
            if progress.wasCanceled():
                break
            update_theme_progress(layer.name(), index, self.tr(u"Підготовка"))
            cache_key = self._theme_cache_key(layer, group)
            cache = self._layer_theme_cache.setdefault(cache_key, {}) if cache_key else {}
            if not enabled:
                cache_hits += 1
                update_theme_progress(layer.name(), index, self.tr(u"Тема Ескіз (з кешу/шаблону)"))
                self._apply_sketch_style(layer, trigger_repaint=False)
                continue
            cached_normative = None
            if cache is not None:
                cached_normative = cache.get("normative")
            if cached_normative is False:
                cache_hits += 1
                sketch_fallbacks += 1
                update_theme_progress(layer.name(), index, self.tr(u"Умовні знаки: нема стилю, Ескіз (з кешу)"))
                self._apply_sketch_style(layer, trigger_repaint=False)
                continue
            if cached_normative is not None:
                cache_hits += 1
                update_theme_progress(layer.name(), index, self.tr(u"Умовні знаки (з кешу)"))
                try:
                    layer.setRenderer(self._clone_renderer(cached_normative))
                except Exception:
                    sketch_fallbacks += 1
                    update_theme_progress(layer.name(), index, self.tr(u"Помилка кешу, Ескіз"))
                    self._apply_sketch_style(layer, trigger_repaint=False)
                continue
            style_path = self._normative_style_path(layer.name())
            if not style_path:
                if cache is not None:
                    cache["normative"] = False
                sketch_fallbacks += 1
                update_theme_progress(layer.name(), index, self.tr(u"Нема стилю, Ескіз (кешовано)"))
                self._apply_sketch_style(layer, trigger_repaint=False)
                continue
            if not os.path.exists(style_path):
                if cache is not None:
                    cache["normative"] = False
                sketch_fallbacks += 1
                update_theme_progress(layer.name(), index, self.tr(u"Файл стилю відсутній, Ескіз (кешовано)"))
                self._apply_sketch_style(layer, trigger_repaint=False)
                continue
            try:
                file_reads += 1
                update_theme_progress(layer.name(), index, self.tr(u"Читання файлу стилю"))
                self._load_symbology_only(layer, style_path)
                renderer = None
                try:
                    renderer = layer.renderer()
                except Exception:
                    renderer = None
                renderer_clone = self._clone_renderer(renderer)
                if cache is not None:
                    cache["normative"] = renderer_clone if renderer_clone is not None else False
                update_theme_progress(layer.name(), index, self.tr(u"Умовні знаки (з файлу, кешовано)"))
            except Exception:
                if cache is not None:
                    cache["normative"] = False
                sketch_fallbacks += 1
                update_theme_progress(layer.name(), index, self.tr(u"Помилка стилю, Ескіз"))
                self._apply_sketch_style(layer, trigger_repaint=False)
                continue
        if canvas is not None and prev_render is not None:
            try:
                canvas.setRenderFlag(prev_render)
            except Exception:
                pass
            try:
                canvas.refresh()
            except Exception:
                pass
        progress.setValue(total)
        try:
            progress.setCancelButtonText(self.tr(u"Закрити"))
        except Exception:
            pass
        self._pump_progress_ui(progress)
        try:
            progress.close()
            progress.deleteLater()
        except Exception:
            pass

    def _load_symbology_only(self, layer: QgsVectorLayer, style_path: str) -> None:
        if layer is None or not style_path:
            return
        data = None
        for encoding in ("utf-8", "cp1251", "utf-8-sig"):
            try:
                with open(style_path, "r", encoding=encoding) as handle:
                    data = handle.read()
                break
            except Exception:
                continue
        if data is None:
            return
        import re
        strip_blocks = [
            "fieldConfiguration",
            "aliases",
            "defaults",
            "constraints",
            "constraintExpressions",
            "attributetableconfig",
            "editable",
            "editform",
            "editforminit",
            "editforminitcode",
            "editforminitcodesource",
            "editforminitfilepath",
            "attributeEditorForm",
            "attributeEditorField",
            "attributeEditorContainer",
            "attributeEditorRelation",
            "attributeEditorQml",
        ]
        for tag in strip_blocks:
            data = re.sub(rf"<{tag}[^>]*>.*?</{tag}>", "", data, flags=re.IGNORECASE | re.DOTALL)
        temp_path = os.path.join(self.plugin_dir, ".normative_style_tmp.qml")
        try:
            with open(temp_path, "w", encoding="utf-8") as handle:
                handle.write(data)
            try:
                categories = QgsMapLayer.StyleCategory.Symbology
                layer.loadNamedStyle(temp_path, categories)
            except Exception:
                layer.loadNamedStyle(temp_path)
        finally:
            try:
                if os.path.exists(temp_path):
                    os.remove(temp_path)
            except Exception:
                pass

    def _apply_sketch_style(self, layer: QgsVectorLayer, trigger_repaint: bool = True) -> None:
        if layer is None:
            return
        cache_key = self._theme_cache_key(layer)
        cache = self._layer_theme_cache.setdefault(cache_key, {}) if cache_key else {}
        sketch_renderer = self._clone_renderer(cache.get("sketch")) if cache else None
        if sketch_renderer is None:
            sketch_renderer = self._build_sketch_renderer(layer)
            if sketch_renderer is None:
                return
            if cache is not None:
                cache["sketch"] = self._clone_renderer(sketch_renderer)
        if sketch_renderer is None:
            return
        try:
            layer.setRenderer(sketch_renderer)
            if trigger_repaint:
                layer.triggerRepaint()
        except Exception:
            return

    def on_toggle_normative_style(self):
        new_enabled = not self._normative_style_enabled
        if common.LOG:
            common.log_calls(common.logFile, f"GeoJsonUa.on_toggle_normative_style(enabled={new_enabled})")
        self._normative_style_enabled = new_enabled
        group = getattr(self.opened_projects, "current_project", None)
        self._apply_normative_style(group, self._normative_style_enabled)
        self._update_normative_style_action()

    def on_toggle_debug_mode(self):
        if common.LOG:
            common.log_calls(common.logFile, f"GeoJsonUa.on_toggle_debug_mode(checked={bool(self.action_debug_mode.isChecked())})")
        self.debug_enabled = bool(self.action_debug_mode.isChecked())
        QSettings().setValue("json_ua/debug", self.debug_enabled)
        common.set_log_enabled(self.debug_enabled)
        if common.LOG:
            common.log_calls(common.logFile, f"GeoJsonUa.on_toggle_debug_mode -> LOG={common.LOG}")
        if self.debug_enabled:
            self._push_info(self.tr(u"Налагоджувальний режим увімкнено."))
        else:
            self._push_info(self.tr(u"Налагоджувальний режим вимкнено."))

    def run(self):
        """Run method that loads and starts the plugin"""
        if common.LOG:
            common.log_calls(common.logFile, "GeoJsonUa.run()")

        if self.dockwidget is None:
            self.dockwidget = GeoJsonUaDockWidget()
            self.dockwidget.closingPlugin.connect(self.onClosePlugin)
            self.dockwidget.request_geojson.connect(self._on_request_geojson)
            self.iface.addDockWidget(Qt.LeftDockWidgetArea, self.dockwidget)
            self._ensure_dockwidget_panel_name()
            self._refresh_current_project_label()
            self.dockwidget.show()
            return

        if self.dockwidget.isVisible():
            self.dockwidget.hide()
        else:
            self.dockwidget.show()


